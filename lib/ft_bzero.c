#include "lib.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = s;
	if (n == 0)
	{
		return ;
	}
	while (i < n)
	{
		*(ptr + i) = 0;
		i++;
	}
}
